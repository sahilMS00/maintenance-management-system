  public boolean validates()
  {
      if(maintian.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Team id");
          maintian.grabFocus();
          return false;
      }
      if(data.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Team Name");
          data.grabFocus();
          return false;
      }
      if(depart.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Team id");
          depart.grabFocus();
          return false;
      }
      if(discrip.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Team id");
          discrip.grabFocus();
          return false;
      }
      if(report.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Team id");
          report.grabFocus();
          return false;
      }
      if(solve.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Team id");
          solve.grabFocus();
          return false;
      }
      if(close.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Team id");
          close.grabFocus();
          return false;
      }
      if(state.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Team id");
          state.grabFocus();
          return false;
      }
      return true;
  }
