    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
     try {
        String selectedMaintenanceID = cmbTeamname.getSelectedItem().toString();
        String sqlstr = "DELETE FROM `manage` WHERE `MaintenanceID` = ?";
        st = conn.prepareStatement(sqlstr);
        st.setString(1, selectedMaintenanceID);

        int rowsDeleted = st.executeUpdate();

        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(deleteForm.this, "Data Deleted Successfully!!");
            // Repopulate the JComboBox after deletion
            populateMaintenanceIDs();
        } else {
            JOptionPane.showMessageDialog(deleteForm.this, "Deletion Unsuccessful");
        }
    } catch (SQLException sqlException) {
        sqlException.printStackTrace();
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

