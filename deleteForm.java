    private void cmbTeamnameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbTeamnameActionPerformed
        // TODO add your handling code here:
        try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/maintenance", "root", "");
        Statement statement = conn.createStatement();
        ResultSet rs1 = statement.executeQuery("SELECT MaintenanceID FROM manage");

        // Clear existing items in the cmbTeamname ComboBox
        cmbTeamname.removeAllItems();

        while (rs1.next()) {
            // Add each item from the database to the ComboBox
            String maintenanceID = rs1.getString("MaintenanceID");
            cmbTeamname.addItem(maintenanceID);
        }

        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_cmbTeamnameActionPerformed

