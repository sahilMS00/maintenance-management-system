    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
         if (validates()) {
            try {
                String sqlstr = "INSERT INTO `manage` (`MaintenanceID`, `Datacomplaint`, `Department`, `Discription`, `Reportedby`, `Solvedby`, `Closingdate`, `State`) " +
                        "VALUES ('" + maintian.getText() + "', '" + data.getText() + "', '" + depart.getText() + "', '" + discrip.getText() + "', '" +
                        report.getText() + "', '" + solve.getText() + "', '" + close.getText() + "', '" + state.getText() + "')";

                st = conn.prepareStatement(sqlstr);
              
            
            if(st.execute(sqlstr)== false)
            {
                JOptionPane.showMessageDialog(addForm.this, "Data inserted Successfully!!");
                this.setVisible(false);
                new MainUI().setVisible(true);
                conn.close();
            }
            else
            {
                JOptionPane.showMessageDialog(addForm.this, "Enter correct Data");
                
            }
         
        }
        catch (SQLException sqlException) {
                    sqlException.printStackTrace();
                }
        
        }
    }//GEN-LAST:event_btnAddActionPerformed

